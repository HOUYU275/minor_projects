public class Switch {
    public static void main(String args[]) {

        int option = 3;
        switch (option) {
            case 0:
                System.out.println("Sandwich");
                break;
            case 5:
                System.out.println("Coke");
                break;
            default:
                System.out.println("Nothing");
                break;
        }
    }
}
